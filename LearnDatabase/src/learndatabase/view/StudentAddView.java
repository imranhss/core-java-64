    // show all student in Table 
    public void showAllStudent() {

        String[] columnsName = {"ID", "Name", "Email", "Con. No."};
        DefaultTableModel tableModel = new DefaultTableModel(columnsName, 0);
        tblShowStudent.setModel(tableModel);

        String sql = "select * from students";

        try {
            PreparedStatement ps = du.getCon().prepareStatement(sql);

            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                int id = rs.getInt("id");
                String name = rs.getString("name");
                String email = rs.getString("email");
                String contactNo = rs.getString("contactNo");

                // Add row to table model
                Object[] rowData = {id, name, email, contactNo};
                tableModel.addRow(rowData);
            }

            rs.close();
            ps.close();
            du.getCon().close();

        } catch (SQLException ex) {
            Logger.getLogger(LearnDatabase.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

