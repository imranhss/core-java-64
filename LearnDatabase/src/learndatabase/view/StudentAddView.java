    private void btnSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSubmitMouseClicked
        // TODO add your handling code here:

        String sql = "insert into students(name, email, contactNo) values(?,?,?)";

        try {
            PreparedStatement ps = du.getCon().prepareStatement(sql);

            String name = txtName.getText().trim();
            String firstChar = String.valueOf(name.charAt(0)).toUpperCase();

            ps.setString(1, firstChar + name.substring(1));
            ps.setString(2, txtEmail.getText().trim().toLowerCase());
            ps.setString(3, txtContactNo.getText().trim());

            ps.executeUpdate();

            ps.close();
            du.getCon().close();

            JOptionPane.showMessageDialog(null, "Student Save Successfully ");
            resetStudentForm();
            showAllStudent();

        } catch (SQLException ex) {
            Logger.getLogger(LearnDatabase.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null, "Student Not Save ");

        }


    }//GEN-LAST:event_btnSubmitMouseClicked

